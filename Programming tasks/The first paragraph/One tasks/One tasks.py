# Действительные числа a и b
a, b = 0.0, 0.0

# Получение данных
a = float(input("Пожалуйста введите число A: "))
b = float(input("Пожалуйста введите число B: "))

# Ответы задачи
print("\n", a, " + ", b, " = ", (a + b))
print("\n", a, " - ", b, " = ", (a - b))
print("\n", a, " * ", b, " = ", (a * b))

